#!/usr/bin/env node
// render-dashboard.mjs — render the cross-feature dashboard from one or more status.json files.
//
// Pure function — no git/gh calls. The `/design-to-code:dashboard` command
// pre-enriches each status.json with PR titles / dates from `gh pr view` before
// passing them in; the test harness passes them in as-is.
//
// Usage:
//   import { renderDashboard } from './render-dashboard.mjs';
//   const md = renderDashboard([statusA, statusB]);
//
// CLI:
//   node scripts/render-dashboard.mjs path/to/status1.json path/to/status2.json

import { readFileSync } from 'node:fs';

/**
 * @param {object[]} statuses - array of per-feature status.json objects
 * @returns {string} markdown
 */
export function renderDashboard(statuses) {
  const features = statuses.map(normalize);
  const cum = cumulative(features);

  return [
    '# Design-to-Code Bridge — Dashboard',
    '',
    `*Generated by \`/design-to-code:dashboard\` at ${new Date().toISOString()}.*`,
    '',
    '## Cumulative',
    '',
    '```',
    `Features bridged:        ${cum.featureCount}`,
    `Total slices merged:     ${cum.slicesMerged}`,
    `Total LOC:               ${formatInt(cum.loc)}`,
    `Components introduced:   ${cum.componentsNew}  (+)`,
    `Components reused:       ${cum.componentsReused}  (-)`,
    `Reuse ratio:             ${cum.reuseRatio}%  (target: ↑ over time)`,
    `Net-new (no primitive):  ${cum.netNew}  (target: ↓ over time)`,
    '```',
    '',
    '## Per-feature table',
    '',
    '| Feature | Status | Started | Closed | Slices | LOC | PRs | Spike | New / Reused |',
    '|---|---|---|---|---|---|---|---|---|',
    ...features.map(featureRow),
    '',
    '## Component +/− diff per feature',
    '',
    ...features.flatMap(componentDiffSection),
  ].join('\n');
}

function normalize(s) {
  const slicesMerged = (s.slices || []).filter((x) => x.merged).length;
  const loc = (s.slices || []).reduce((acc, x) => acc + (x.merged ? (x.loc || 0) : 0), 0);
  const tier = s.componentMap?.byTier || {};
  const sumNew = (t) => (tier[t]?.new ?? 0);
  const sumReused = (t) => (tier[t]?.reused ?? 0);
  const componentsNew = sumNew('base') + sumNew('ds') + sumNew('custom-shared') + sumNew('custom-page') + sumNew('net-new');
  const componentsReused = sumReused('base') + sumReused('ds') + sumReused('custom-shared') + sumReused('custom-page') + sumReused('net-new');
  const netNew = sumNew('net-new');

  const prs = (s.slices || []).filter((x) => x.pr).map((x) => x.pr).sort((a, b) => a - b);
  const prRange = prs.length === 0 ? '—'
    : prs.length === 1 ? `#${prs[0]}`
    : `#${prs[0]}–#${prs[prs.length - 1]}`;

  return {
    feature: s.feature,
    phase: phaseLabel(s),
    started: dateOnly(s.started_at),
    closed: dateOnly(s.completed_at) || '—',
    slicesMerged,
    sliceTotal: (s.slices || []).length,
    loc,
    prRange,
    spikePath: s.specDocPath || '—',
    componentsNew,
    componentsReused,
    netNew,
    tier,
  };
}

function cumulative(features) {
  return {
    featureCount: features.length,
    slicesMerged: features.reduce((a, f) => a + f.slicesMerged, 0),
    loc: features.reduce((a, f) => a + f.loc, 0),
    componentsNew: features.reduce((a, f) => a + f.componentsNew, 0),
    componentsReused: features.reduce((a, f) => a + f.componentsReused, 0),
    netNew: features.reduce((a, f) => a + f.netNew, 0),
    reuseRatio: (() => {
      const n = features.reduce((a, f) => a + f.componentsNew, 0);
      const r = features.reduce((a, f) => a + f.componentsReused, 0);
      const tot = n + r;
      return tot === 0 ? 0 : Math.round((r / tot) * 100);
    })(),
  };
}

function featureRow(f) {
  return `| ${f.feature} | ${f.phase} | ${f.started} | ${f.closed} | ${f.slicesMerged}${f.sliceTotal && f.sliceTotal !== f.slicesMerged ? `/${f.sliceTotal}` : ''} | ${formatInt(f.loc)} | ${f.prRange} | ${f.spikePath} | +${f.componentsNew} / −${f.componentsReused} |`;
}

function componentDiffSection(f) {
  const tier = f.tier || {};
  const lines = [
    `### ${f.feature}`,
    '',
  ];
  const tiers = [
    ['custom-shared', 'new custom-shared'],
    ['custom-page',   'new custom-page'],
    ['net-new',       'net-new'],
    ['ds',            'new ds wrapper'],
  ];
  for (const [k, label] of tiers) {
    const v = tier[k]?.new ?? 0;
    if (v > 0) lines.push(`- **+ ${v} ${label}**`);
  }
  const baseR = tier.base?.reused ?? 0;
  const dsR = tier.ds?.reused ?? 0;
  if (baseR > 0) lines.push(`- **− ${baseR} reused base**`);
  if (dsR > 0) lines.push(`- **− ${dsR} reused ds**`);
  lines.push('');
  return lines;
}

function phaseLabel(s) {
  if (s.phase === 'complete') return 'complete';
  if (s.phase === 'slice') {
    const total = (s.slices || []).length;
    const merged = (s.slices || []).filter((x) => x.merged).length;
    return `slice (${merged}/${total})`;
  }
  return s.phase || 'unknown';
}

function dateOnly(iso) {
  if (!iso) return null;
  return iso.slice(0, 10);
}

function formatInt(n) {
  return n.toLocaleString('en-US');
}

// CLI
if (import.meta.url === `file://${process.argv[1]}`) {
  const paths = process.argv.slice(2);
  if (paths.length === 0) {
    console.error('usage: node render-dashboard.mjs <status.json> [<status.json> ...]');
    process.exit(2);
  }
  const statuses = paths.map((p) => JSON.parse(readFileSync(p, 'utf8')));
  console.log(renderDashboard(statuses));
}
